// Funciones helper para filtros
export const getSubcategorias = (categoriaId) => {
  const cat = categorias.find(c => c.id === categoriaId)
  return cat ? cat.subcategorias : []
}

export const getUniquePrecios = (productos) => {
  const precios = productos.map(p => p.precio).filter(p => p !== null)
  return {
    min: Math.min(...precios),
    max: Math.max(...precios)
  }
}